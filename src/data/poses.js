export const getDifficultyColor = (level) => {
  switch (level) {
    case 'Beginner': return '#00E676';
    case 'Intermediate': return '#FFD600';
    case 'Advanced': return '#FF5252';
    default: return '#B0BEC5';
  }
};

export const getPoseById = (id) => {
  return YOGA_POSES.find(pose => pose.id === id);
};

export const getPosesByLevel = (level) => {
  if (level === 'All') return YOGA_POSES;
  return YOGA_POSES.filter(pose => pose.level === level);
};
